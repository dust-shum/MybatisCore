package com.dust.pojo;

/**
 * @author DUST
 * @description 参数映射
 * @date 2022/5/25
 */
public class ParameterMapping {

    private String name;

    public ParameterMapping(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
